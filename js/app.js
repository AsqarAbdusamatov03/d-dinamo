function openNav() {
    document.getElementById("nav-div").style.display = "block";
    document.getElementById("close").style.display = "block";
    document.getElementById("onclik").style.display= "none";
}

function closeNav() {
    document.getElementById("nav-div").style.display = "none";
    document.getElementById("close").style.display = "none";
    document.getElementById("onclik").style.display= "block";
}
